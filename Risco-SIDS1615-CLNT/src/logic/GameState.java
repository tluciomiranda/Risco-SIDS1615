	public void deliverTerritories(){
		int tempPlayer = 0;
		for(int i = 0; i < this.cardDeck.size();++i){
			int t = this.cardDeck.get(i).getTerritory();
			
			this.territories.get(t).setOwner(tempPlayer);
			
			if(tempPlayer== (this.nPlayers-1)){
				tempPlayer = 0;
			}
			else{
				++tempPlayer;
			}
			
		}
	}
	
